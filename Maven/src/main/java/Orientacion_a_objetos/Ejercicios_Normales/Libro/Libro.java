package Orientacion_a_objetos.Ejercicios_Normales.Libro;

public class Libro {
    // Atributo
    String isbn;
    String titulo;
    String autor;
    int numPagina;

    // Contructor



    // Metodo

    @Override
    public String toString() {
        return "Libro{" +
                "isbn='" + isbn + '\'' +
                ", titulo='" + titulo + '\'' +
                ", autor='" + autor + '\'' +
                ", numPagina=" + numPagina +
                '}';
    }
}
